# Normal triangle with ascending order
# *
# **
# ***
# ****
# *****

# =========================Normal Star Pattern====================================================
for i in range(1, 6):
    for j in range(1, 6):
        if j <= i:
            print("*", end='')
        else:
            print('', end='')
    print()

print("-------------------------------------------------------------------------------------------\n")

# ============================Mirrored Normal Star pattern=========================================
for i in range(1, 6):
    for j in range(1, 6):
        if j >= i:
            print("*", end='')
        else:
            print('', end='')
    print()

print("-------------------------------------------------------------------------------------------\n")

# ==========================Increasing from center================================================

n = 5
for i in range(1, n+1):
    for j in range(n - i):
        print(' ', end='')
    for k in range(2 * i - 1):
        print('*', end='')
    print()

print("-------------------------------------------------------------------------------------------\n")


